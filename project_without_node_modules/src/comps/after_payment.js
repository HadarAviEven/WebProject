import React, { Component } from 'react';


class AfterPayment extends Component {
    state = { objects: [] };

    componentDidMount() {
        let temp_ar = [];
        let keys = Object.keys(localStorage);
        for (let key of keys) {
            temp_ar.push(localStorage.getItem(key));
        }
        this.setState({ objects: temp_ar });
    }

    renderTableData() {
        let item_number = 1;
        return this.state.objects.map((item) => {
            return (
                <tr key={item}>
                    <td>{item_number++}</td>
                    <td>{item}</td>
                    <td>
                        <span className=" bg-danger p-2 rounded text-dark  font-weight-bold">On Delivery</span>
                    </td>
                </tr>
            )
        })
    }

    render() {
        return (
            <div >
                <div className="container-fluid bg-secondary p-4">
                    <div className="container">

                        <table className="table table-striped table-dark">
                            <thead>
                                <tr>
                                    <th>#</th>
                                    <th>Item</th>
                                    <th>Delete</th>
                                </tr>
                            </thead>
                            <tbody>
                                {this.renderTableData()}
                            </tbody>
                        </table>
                    </div>

                </div >
            </div >
        );
    }
}
export default AfterPayment;