import React, { Component } from 'react';
import { Link } from "react-router-dom";

class JewelrySingle extends Component {

    handleOnClick = () => {
        localStorage.setItem(this.props.item.title, this.props.item.title);
    };

    render() {
        let item = this.props.item;
        return (
            <div className="col-lg-6 mb-4">
                <div className="text-left bg-white border border-dark p-2 rounded font-italic">
                    <div className="float-left m-2 inner">
                        <img src={"/images/" + item.type + "/" + item.style + item.image} width="128" height="128" />
                    </div>
                    <h2>{item.title}</h2>
                    <div>Style: {item.style}</div>
                    <div>Price: {item.price}$</div>
                    <div className="py-1">
                        <Link className="p-1" to={"/jewelry/" + item.id}>
                            <button className="btn btn-info">More Info</button>
                        </Link>
                        <button onClick={this.handleOnClick} className="btn btn-warning text-white">Add to Cart</button>
                    </div>
                </div>
            </div>
        );
    }
}
export default JewelrySingle;
