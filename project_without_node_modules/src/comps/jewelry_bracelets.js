import React, { Component } from 'react';
import JewelrySingle from './jewelry_single';

class JewelryBracelets extends Component {
    state = { bracelets_ar: [], braceletsSearch_ar: [] };

    componentDidMount() {
        let myUrl = "https://raw.githubusercontent.com/HadarAviEven/web_project/master/JewelryFinal.json"
        var type_ar = [];
        fetch(myUrl)
            .then(resp => resp.json())
            .then(data => {
                type_ar = data.filter(item => {
                    return (item.type === "Bracelets");
                })
                this.setState({ bracelets_ar: type_ar, braceletsSearch_ar: type_ar });
            })
    }

    onChangeSearch = () => {
        let searchQ = this.refs.id_search.value;
        let temp_ar = this.state.bracelets_ar.filter(item => {
            return (item.title.toLowerCase().indexOf(searchQ.toLowerCase()) >= 0);
        })
        this.setState({ braceletsSearch_ar: temp_ar });
    }

    render() {
        return (
            <div className="container-fluid bg-secondary">
                <div className="container">
                    <div className="py-3">
                        <label className="text-warning px-2 font-weight-bold">Search:</label>
                        <input ref={"id_search"} type="text" onInput={this.onChangeSearch} />
                    </div>
                    {(this.state.braceletsSearch_ar.length > 0) ? "" : <img src="images/loading.gif" />}
                    <div className="row">
                        {this.state.braceletsSearch_ar.map(item => {
                            return (
                                <JewelrySingle item={item} key={item.id} />
                            )
                        })}
                    </div>
                </div>
            </div>
        );
    }
}
export default JewelryBracelets;